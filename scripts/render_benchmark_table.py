def main() -> None:
    args = parse_args()
    csv_path = Path(args.csv_path)
    html_path = Path(args.html_path)

    rows = read_rows(csv_path)
    html_path.write_text(render_html(rows), encoding="utf-8")


if __name__ == "__main__":
    main()
