import argparse
import csv
import html
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Render benchmark run metrics CSV as a standalone HTML table."
    )
    parser.add_argument("csv_path", help="Input CSV generated by run_opencode_task.ps1")
    parser.add_argument(
        "html_path",
        nargs="?",
        default="benchmark-runs.html",
        help="Output HTML path",
    )
    return parser.parse_args()


def read_rows(csv_path: Path) -> list[dict[str, str]]:
    with csv_path.open("r", encoding="utf-8-sig", newline="") as handle:
        reader = csv.DictReader(handle)
        rows = list(reader)

    def duration_key(row: dict[str, str]) -> int:
        try:
            return int(row.get("duration_ms", "0") or "0")
        except ValueError:
            return 0

    rows.sort(
        key=lambda row: (
            row.get("task_name", ""),
            row.get("model", ""),
            duration_key(row),
        )
    )
    return rows


def format_number(value: str) -> str:
    if value == "":
        return ""
    try:
        return f"{int(value):,}"
    except ValueError:
        return value


def build_table_rows(rows: list[dict[str, str]]) -> str:
    rendered = []
    for row in rows:
        output_path = row.get("output_path", "")
        output_name = Path(output_path).name if output_path else ""
        rendered.append(
            "<tr>"
            f"<td>{html.escape(row.get('task_name', ''))}</td>"
            f"<td>{html.escape(row.get('model', ''))}</td>"
            f"<td>{html.escape(row.get('variant', ''))}</td>"
            f"<td>{html.escape(row.get('duration_hms', ''))}</td>"
            f"<td>{html.escape(row.get('cost_usd', ''))}</td>"
            f"<td>{format_number(row.get('tokens_input', ''))}</td>"
            f"<td>{format_number(row.get('tokens_output', ''))}</td>"
            f"<td>{format_number(row.get('tokens_reasoning', ''))}</td>"
            f"<td>{format_number(row.get('tokens_cache_read', ''))}</td>"
            f"<td>{format_number(row.get('tokens_cache_write', ''))}</td>"
            f"<td>{format_number(row.get('tokens_total', ''))}</td>"
            f"<td>{html.escape(str(row.get('exit_code', '')))}</td>"
            f"<td>{html.escape(row.get('started_at', ''))}</td>"
            f"<td class=\"path\" title=\"{html.escape(row.get('task_prompt', ''))}\">{html.escape(output_name)}</td>"
            "</tr>"
        )
    return "\n".join(rendered)


